#divsao -> /;//;%

num1 = 5
num2 = 3

result = num1 / num2 #retorna a divisao completa -> parte inteira e decima
print(f"{result}")
result = num1 // num2 #retorna apenas a parte inteira
print(f"{result}")
result = num1 % num2 #retorna apenas o resto da divisao
print(f"{result}")

result = num1 ** num2 #exponenciacao
print(f"{result}")